/**
 * Get location by ID
 * @param {string} locationId - The location ID (e.g., 'SH1', 'LT1')
 * @returns {object|null} - Location object or null if not found
 */
window.getLocationById = function(locationId) {
    return window.UENR_LOCATIONS_MASTER[locationId] || null;
};

/**
 * Get all locations organized by category
 * @returns {object} - Grouped locations by category
 */
window.getLocationsByCategory = function() {
    const grouped = {};
    Object.values(window.UENR_LOCATIONS_MASTER).forEach(location => {
        if (!grouped[location.category]) {
            grouped[location.category] = [];
        }
        grouped[location.category].push(location);
    });
    return grouped;
};

/**
 * Get all location IDs
 * @returns {array} - Array of location IDs
 */
window.getAllLocationIds = function() {
    return Object.keys(window.UENR_LOCATIONS_MASTER);
};
